#include <iostream>

int main()
{
	int    num = 30;
	std::cout << "num:\t\t"    << num << std::endl;
    double distancia = 1.34654e3, diametro = 2.4;
	std::cout << "distancia:\t"  << distancia << std::endl;
	std::cout << "diametro:\t"  << diametro << std::endl;
    bool   b1         = true;
	bool   b2         = false;
	std::cout << "b1:\t\t"  << b1 << "\nb2:\t\t" << b2 << std::endl;
    char   c         = 'a';
	std::cout << "c:\t\t"  << c << std::endl;
	return 0;
}

